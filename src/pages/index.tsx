import Head from "next/head";
import Link from "next/link";
import { useSession, signIn } from "next-auth/react";
import { useRouter } from "next/navigation";
import { useEffect } from "react";
export default function Home() {
  //TODO: turn this into auth page
  const { data: session } = useSession();
  // eslint-disable-next-line @typescript-eslint/unbound-method
  const { push } = useRouter();

  useEffect(() => {
    if (session) {
      push("/typing");
    }
  }, [push, session]);
  return (
    <>
      <Head>
        <title>TeamTS</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="align-center flex h-[100vh] flex-col items-center justify-center">
        {session ? (
          <Link href="/typing">
            <div className="rounded-pill w-[170px] bg-blue-500 px-4 py-2 font-bold text-white">
              Go to typing test
            </div>
          </Link>
        ) : (
          <button
            // eslint-disable-next-line @typescript-eslint/no-misused-promises
            onClick={() => signIn()}
            className="rounded-pill w-[170px] bg-blue-500 px-4 py-2 font-bold text-white"
          >
            Sign in
          </button>
        )}
        <div className="my-10 flex w-[60vw] flex-col items-center gap-2">
          <div>{`Cant Lie, not the best submission but life got in the way`}</div>
          <div>
            If I had more time would try to make it so twitch chat can join in
            simultaneously, and would have leader board and stuff (kind of like
            typeracer)
          </div>
          <div className=" my-10">
            btw added twitch auth just so it falls as twitch category lol
          </div>
        </div>
      </div>
    </>
  );
}
